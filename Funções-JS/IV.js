/**
 * Objeto calculadora com operações básicas
 */
const calculadora = {
  /**
   * Soma dois números
   */
  somar: function(a, b) {
    return a + b;
  },
  
  /**
   * Subtrai dois números
   */
  subtrair: function(a, b) {
    return a - b;
  },
  
  /**
   * Multiplica dois números
   */
  multiplicar: function(a, b) {
    return a * b;
  },
  
  /**
   * Divide dois números
   */
  dividir: function(a, b) {
    return a / b;
  }
};